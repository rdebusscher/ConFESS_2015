package be.c4j.demo.model;

/**
 *
 */
public class Person {
    private String name;
    private Gender gender;
    private Integer age;

    public Person() {
        this(null, null, null);
    }

    public Person(String name, Gender gender, Integer age) {
        this.name = name;
        this.gender = gender;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Gender getGender() {
        return gender;
    }

    public void setGender(Gender gender) {
        this.gender = gender;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (!(o instanceof Person)) {
            return false;
        }

        Person person = (Person) o;

        if (!name.equalsIgnoreCase(person.name)) {
            return false;
        }

        return true;
    }

    @Override
    public int hashCode() {
        return name.toLowerCase().hashCode();
    }

    public static Person clonePerson(Person person) {
        return new Person(person.getName(), person.getGender(), person.getAge());
    }
}
